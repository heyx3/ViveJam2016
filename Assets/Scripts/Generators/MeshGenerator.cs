	private IEnumerator RunGenerator(bool fromScratch)
	{
		//Run the initial generation if necessary.
		if (fromScratch)
		{
			GridGenerator gen = new GridGenerator(XSize, YSize, ZSize, XOffset, YOffset, ZOffset);
			while (!gen.IsDone)
				yield return null;
			Voxels = gen.Voxels;
		}

		//Generate the mesh data on a new thread.
		List<Vector3> poses = new List<Vector3>();
		List<int> indices = new List<int>();
		System.Threading.Thread thr = new System.Threading.Thread(() => GenerateMeshData(poses, indices));
		thr.Start();
		while (thr.IsAlive)
			yield return null;

		//TODO: If there is too much mesh data, split this cell into four smaller ones.

		//TODO: Calculate normals using Central Differences.

		//Put the data into the mesh.
		if (mesh == null)
		{
			mesh = new Mesh();
		}
		else
		{
			mesh.Clear();
		}
		mesh.vertices = poses.ToArray();
		mesh.triangles = indices.ToArray();
		mesh.RecalculateNormals();
		mesh.UploadMeshData(false);

		MeshCollider mc = GetComponent<MeshCollider>();
		if (mc != null)
			mc.sharedMesh = mesh;

		cr = null;
	}
	private void GenerateMeshData(List<Vector3> poses, List<int> indices)
	{
		float[] gridVals = new float[8];
		const float isoSurface = 0.6f;
		List<Triangle> tris = new List<Triangle>();

		for (int x = 0; x < Voxels.GetLength(0) - 1; ++x)
		{
			for (int y = 0; y < Voxels.GetLength(1) - 1; ++y)
			{
				for (int z = 0; z < Voxels.GetLength(2) - 1; ++z)
				{
					gridVals[0] = Voxels[x, y, z];
					gridVals[1] = Voxels[x + 1, y, z];
					gridVals[2] = Voxels[x + 1, y + 1, z];
					gridVals[3] = Voxels[x, y + 1, z];
					gridVals[4] = Voxels[x, y, z + 1];
					gridVals[5] = Voxels[x + 1, y, z + 1];
					gridVals[6] = Voxels[x + 1, y + 1, z + 1];
					gridVals[7] = Voxels[x, y + 1, z + 1];

					tris.Clear();
					CalculateTris(gridVals, isoSurface, tris);

					//TODO: Use a more efficient method that finds duplicate vertices.
					Vector3 offset = new Vector3((float)(x + XOffset - (XOffset / XSize)),
												 (float)(y + YOffset - (YOffset / YSize)),
												 (float)(z + ZOffset - (ZOffset / ZSize)));
					for (int i = 0; i < tris.Count; ++i)
					{
						poses.Add(tris[i].P1 + offset);
						indices.Add(poses.Count - 1);

						poses.Add(tris[i].P2 + offset);
						indices.Add(poses.Count - 1);

						poses.Add(tris[i].P3 + offset);
						indices.Add(poses.Count - 1);
					}
				}
			}
		}
	}
	private struct Triangle { public Vector3 P1, P2, P3; }
	private void CalculateTris(float[] gridVals, float isoSurface, List<Triangle> outTris)
	{
		Vector3[] vertList = new Vector3[12];
		for (int i = 0; i < 12; ++i)
			vertList[i] = new Vector3(float.NaN, 0.0f, 0.0f);

		uint cubeIndex = 0;
		if (gridVals[0] < isoSurface) cubeIndex |= 1;
		if (gridVals[1] < isoSurface) cubeIndex |= 2;
		if (gridVals[2] < isoSurface) cubeIndex |= 4;
		if (gridVals[3] < isoSurface) cubeIndex |= 8;
		if (gridVals[4] < isoSurface) cubeIndex |= 16;
		if (gridVals[5] < isoSurface) cubeIndex |= 32;
		if (gridVals[6] < isoSurface) cubeIndex |= 64;
		if (gridVals[7] < isoSurface) cubeIndex |= 128;

		int edgeValue = edgeTable[cubeIndex];

		if (edgeValue == 0)
			return;
		
		//Calculate the vertices along any edges that contain a surface bound.
		if ((edgeValue & 1) != 0)
		{
			vertList[0] = CalcVertex(isoSurface, posOffset[0], posOffset[1], gridVals[0], gridVals[1]);
		}
		if ((edgeValue & 2) != 0)
		{
			vertList[1] = CalcVertex(isoSurface, posOffset[1], posOffset[2], gridVals[1], gridVals[2]);
		}
		if ((edgeValue & 4) != 0)
		{
			vertList[2] = CalcVertex(isoSurface, posOffset[2], posOffset[3], gridVals[2], gridVals[3]);
		}
		if ((edgeValue & 8) != 0)
		{
			vertList[3] = CalcVertex(isoSurface, posOffset[3], posOffset[0], gridVals[3], gridVals[0]);
		}
		if ((edgeValue & 16) != 0)
		{
			vertList[4] = CalcVertex(isoSurface, posOffset[4], posOffset[5], gridVals[4], gridVals[5]);
		}
		if ((edgeValue & 32) != 0)
		{
			vertList[5] = CalcVertex(isoSurface, posOffset[5], posOffset[6], gridVals[5], gridVals[6]);
		}
		if ((edgeValue & 64) != 0)
		{
			vertList[6] = CalcVertex(isoSurface, posOffset[6], posOffset[7], gridVals[6], gridVals[7]);
		}
		if ((edgeValue & 128) != 0)
		{
			vertList[7] = CalcVertex(isoSurface, posOffset[7], posOffset[4], gridVals[7], gridVals[4]);
		}
		if ((edgeValue & 256) != 0)
		{
			vertList[8] = CalcVertex(isoSurface, posOffset[0], posOffset[4], gridVals[0], gridVals[4]);
		}
		if ((edgeValue & 512) != 0)
		{
			vertList[9] = CalcVertex(isoSurface, posOffset[1], posOffset[5], gridVals[1], gridVals[5]);
		}
		if ((edgeValue & 1024) != 0)
		{
			vertList[10] = CalcVertex(isoSurface, posOffset[2], posOffset[6], gridVals[2], gridVals[6]);
		}
		if ((edgeValue & 2048) != 0)
		{
			vertList[11] = CalcVertex(isoSurface, posOffset[3], posOffset[7], gridVals[3], gridVals[7]);
		}

		for (int i = 0; triTable[cubeIndex, i] != -1; i += 3)
		{
			Triangle tri = new Triangle();
			tri.P1 = vertList[triTable[cubeIndex, i]];
			tri.P2 = vertList[triTable[cubeIndex, i + 1]];
			tri.P3 = vertList[triTable[cubeIndex, i + 2]];

			outTris.Add(tri);
		}
	}
	private Vector3 CalcVertex(float isoLevel, Vector3 p1, Vector3 p2, float valP1, float valP2)
	{
		if (IsLessThan(p2, p1))
		{
			Vector3 temp3 = p1;
			float tempF = valP1;
			p1 = p2;
			valP1 = valP2;
			p2 = temp3;
			valP2 = tempF;
		}

		if (Mathf.Abs(valP1 - valP2) > 0.00001f)
			return p1 + ((p2 - p1) * (isoLevel - valP1) / (valP2 - valP1));
		else return p1;

		//Old buggier method:
		//Edge cases.
		if (Mathf.Abs(isoLevel - valP1) < 0.00001f)
			return p1;
		if (Mathf.Abs(isoLevel - valP2) < 0.00001f)
			return p2;
		if (Mathf.Abs(valP1 - valP2) < 0.00001f)
			return p1;

		float mu = (isoLevel - valP1) / (valP2 - valP1);
		return p1 + (mu * (p2 - p1));
	}
	private bool IsLessThan(Vector3 p1, Vector3 p2)
	{
		if (p1.x < p2.x)
			return true;
		else if (p1.x > p2.x)
			return false;

		if (p1.y < p2.y)
			return true;
		else if (p1.y > p2.y)
			return false;

		if (p1.z < p2.z)
			return true;
		else if (p1.z > p2.z)
			return false;

		return false;
	}


	public void Explode(Vector3 center, float radius)
	{
		if (cr != null)
			return;


		Vector3 minF = center - new Vector3(radius, radius, radius),
				maxF = center + new Vector3(radius, radius, radius);
		Vector3i min = new Vector3i(Mathf.Max((int)minF.x, XOffset),
									Mathf.Max((int)minF.y, YOffset),
									Mathf.Max((int)minF.z, ZOffset)),
				 max = new Vector3i(Mathf.Min((int)maxF.x + 1, XOffset + XSize - 1),
									Mathf.Min((int)maxF.y + 1, YOffset + YSize - 1),
									Mathf.Min((int)maxF.z + 1, ZOffset + ZSize - 1));

		int nChanged = 0;
		for (int x = min.x; x <= max.x; ++x)
		{
			for (int y = min.y; y <= max.y; ++y)
			{
				for (int z = min.z; z <= max.z; ++z)
				{
					Vector3 pos = new Vector3(x, y, z);
					Vector3i relPos = new Vector3i(x - XOffset, y - YOffset, z - ZOffset);
					if (Voxels[relPos.x, relPos.y, relPos.z] > 0.0f &&
						pos.DistanceSqr(center) < (radius * radius))
					{
						Voxels[relPos.x, relPos.y, relPos.z] = 0.0f;
						nChanged += 1;
					}
				}
			}
		}

		if (nChanged > 0)
		{
			cr = StartCoroutine(RunGenerator(false));
		}
	}
